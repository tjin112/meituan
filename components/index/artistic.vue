<template>
  <section class="m-istyle">
    <dl @mouseover="over">
      <dt>有格调</dt>
      <dd :class="{active:kind==='all'}" kind="all" keyword="景点">全部</dd>
      <dd :class="{active:kind==='part'}" kind="part" keyword="美食">约会聚餐</dd>
      <dd :class="{active:kind==='spa'}"  kind="spa" keyword="丽人">丽人SPA</dd>
      <dd :class="{active:kind==='movie'}" kind="movie" keyword="电影">电影演出</dd>
      <dd :class="{active:kind==='travel'}" kind="travel"  keyword="旅游">品质出游</dd>
    </dl>
  
    <ul class="ibody">
      
      <li v-for="item in cur" :key="item.title">
         <a :href="'/products?keyword='+encodeURIComponent(item.title)">
        <el-card :body-style="{ padding: '0px' }" shadow="never">
          <img :src="item.img"  class="image">
          <ul class="cbody">
            <li class="title">{{ item.title }}</li>
            <li class="pos"><span>{{ item.pos }}</span></li>
            <li class="price">￥<em>{{ item.price }}</em><span>/起</span></li>
          </ul>
        </el-card></a>
      </li>
    </ul>
  </section>
</template>
<script>
export default {
  data: () => {
    return {
      kind: 'all',
      list: {
        all: [],
        part: [],
        spa: [],
        movie: [],
        travel: []
      }
    }
  },
  computed: {
    cur: function () {
      return this.list[this.kind]
    }
  },

  methods: {
    over: async function (e) {
      let dom = e.target
      let tag = dom.tagName.toLowerCase()
      let self = this
      // console.log(dom.tagName)
      if (tag === 'dd') {
        //getAttribute() 获取鼠标悬停在的dom的属性, 以下为获得kind的值 并将其赋值给this.kind
        this.kind = dom.getAttribute('kind')
        // console.log('kind is ' + this.kind)
        let keyword = dom.getAttribute('keyword')
        let {status,data:{count,pois}}=await self.$axios.get('/search/resultsByKeywords',{
          params:{
            keyword,
            city:self.$store.state.geo.position.city
          }
        })
        if(status===200&&count>0){
          //对后端数据进行过滤

          let r= pois.filter(item=>item.photos.length).map(item=>{
            return {
              title:item.name,
              pos:item.type.split(';')[0],
              price:item.biz_ext.cost||'暂无',
              img:item.photos[0].url,
              url:'http://baidu.com'
            }
          })
          self.list[self.kind]=r.slice(0,9)
          console.log(self.list[self.kind])
        }else{
          self.list[self.kind]=[]
        }
      }
    }
  },

  async mounted(){
    let self = this
       let {status,data:{count,pois}}=await self.$axios.get('/search/resultsByKeywords',{
          params:{
            keyword:'景点',
            city:self.$store.state.geo.position.city
          }
        })
        if(status===200&&count>0){
          //对后端数据进行过滤
          //filter函数 是将符合条件的数据 创建在新的数组中
          //map函数  每次使用filter的时候 都会执行map函数, return 了一些属性到r
          let r= pois.filter(item=>item.photos.length).map(item=>{
            return {
              title:item.name,
              pos:item.type.split(';')[0],
              price:item.biz_ext.cost||'暂无',
              img:item.photos[0].url,
              url:'//abc.com'
            }
          })
          self.list[self.kind]=r.slice(0,9)
          console.log(r.slice(0,9))
        }else{
          self.list[self.kind]=[]
        }
  }

}
</script>
